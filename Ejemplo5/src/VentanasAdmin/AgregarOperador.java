    /**
     * EVENTO: Click en botón "Confirmar"
     * -----------------------------------------------------------------------
     * Se ejecuta cuando el usuario hace clic en el botón "Confirmar".
     * 
     * FLUJO:
     * 1. Validar que los campos no estén vacíos
     * 2. Validar que el código no exista
     * 3. Mostrar diálogo de confirmación
     * 4. Crear el nuevo operador
     * 5. Agregarlo al arreglo global
     * 6. Guardar en el archivo
     * 7. Cerrar esta ventana
     */
    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        // PASO 1: Validar que todos los campos estén llenos
        String[] datos = validarCamposLlenos();
        if (datos == null) {
            return; // Si la validación falla, salir del método
        }
        
        // Extraer los datos validados del arreglo
        String codigo = datos[0];
        String nombre = datos[1];
        String carrera = datos[2];
        String contrasena = datos[3];
        
        // PASO 2: Validar que el código no exista
        if (!validarCodigoNoExiste(codigo)) {
            return; // Si el código ya existe, salir del método
        }
        
        // PASO 3: Mostrar diálogo de confirmación
        int confirmacion = JOptionPane.showConfirmDialog(this, 
            "¿Está seguro de agregar el nuevo operador?\n\n" +
            "Código: " + codigo + "\n" +
            "Nombre: " + nombre + "\n" +
            "Carrera: " + carrera,
            "Confirmar creación", 
            JOptionPane.YES_NO_OPTION);
        
        // PASO 4: Verificar si el usuario confirmó
        if (confirmacion != JOptionPane.YES_OPTION) {
            return; // Si no confirmó, salir del método
        }
        
        // PASO 5: Crear el nuevo operador
        // El primer parámetro (2) es el rol de Operador
        Usuario nuevoOperador = new Usuario(2, codigo, contrasena, nombre, carrera);
        
        // PASO 6: Verificar que hay espacio en el arreglo global
        if (Inicio.totalUsuarios < Inicio.usuarios.length) {
            // Agregar el nuevo operador al arreglo global
            Inicio.usuarios[Inicio.totalUsuarios] = nuevoOperador;
            Inicio.totalUsuarios++; // Incrementar el contador
            
            // PASO 7: Guardar los cambios en el archivo
            guardarCambios();
            
            // PASO 8: Mostrar mensaje de éxito
            JOptionPane.showMessageDialog(this, 
                "Operador agregado exitosamente", "Éxito", 
                JOptionPane.INFORMATION_MESSAGE);
            
            // PASO 9: Cerrar esta ventana
            this.dispose();
        } else {
            // Si no hay espacio, mostrar error
            JOptionPane.showMessageDialog(this, 
                "No hay espacio para más usuarios", "Error", 
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnConfirmarActionPerformed

