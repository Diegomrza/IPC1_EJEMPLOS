    /**
     * MÉTODO: validarCamposLlenos()
     * -----------------------------------------------------------------------
     * Valida que todos los campos obligatorios estén llenos.
     * 
     * @return arreglo de Strings con [codigo, nombre, carrera, contrasena]
     *         o null si hay algún error
     */
    private String[] validarCamposLlenos() {
        // Obtener el texto de los campos (sin espacios al inicio/final)
        String codigo = txtCodigo.getText().trim();
        String nombre = txtNombre.getText().trim();
        String carrera = txtCarrera.getText().trim();
        String contrasena = new String(txtContrasena.getPassword()).trim();
        
        // Verificar que ningún campo esté vacío
        if (codigo.isEmpty() || nombre.isEmpty() || carrera.isEmpty() || contrasena.isEmpty()) {
            // Mostrar mensaje de advertencia
            JOptionPane.showMessageDialog(this, 
                "Todos los campos son obligatorios", "Validación", 
                JOptionPane.WARNING_MESSAGE);
            return null; // Retornar null indica que la validación falló
        }
        
        // Retornar los datos en un arreglo
        return new String[] {codigo, nombre, carrera, contrasena};
    }
    
    /**
     * MÉTODO: validarCodigoNoExiste()
     * -----------------------------------------------------------------------
     * Valida que el código ingresado no exista en el sistema.
     * 
     * @param codigo - Código a verificar
     * @return true si el código NO existe (es válido), false si ya existe
     * 
     * ¿Por qué es importante esta validación?
     * Cada usuario debe tener un código único. Si permitimos códigos duplicados,
     * habría problemas al buscar o modificar usuarios.
     */
    private boolean validarCodigoNoExiste(String codigo) {
        // Recorrer el arreglo global de usuarios
        for (int i = 0; i < Inicio.totalUsuarios; i++) {
            // Verificar que el usuario existe y tiene el mismo código
            if (Inicio.usuarios[i] != null && 
                Inicio.usuarios[i].getCodigo().equals(codigo)) {
                // Si encontramos el código, mostrar error
                JOptionPane.showMessageDialog(this, 
                    "El código ya existe", "Error", JOptionPane.ERROR_MESSAGE);
                return false; // Código duplicado
            }
        }
        return true; // Código válido
    }
    
