    /**
     * MÉTODO: filtrarOperadores()
     * -----------------------------------------------------------------------
     * Filtra la tabla de operadores según el texto que el usuario escribe.
     * 
     * ¿Cómo funciona?
     * - Si el campo está vacío, muestra todos los operadores
     * - Si hay texto, muestra solo las filas que contienen ese texto
     * 
     * El filtro busca en TODAS las columnas (código, nombre y carrera).
     * 
     * (?i) = case insensitive (no distingue mayúsculas de minúsculas)
     * Ejemplo: si escribes "ing", mostrará "Ingeniería" o "INGENIERÍA"
     */
    private void filtrarOperadores() {
        // Obtener el texto del campo de búsqueda (sin espacios al inicio/final)
        String texto = txtBusquedaOperador.getText().trim();
        
        if (texto.length() == 0) {
            // Si no hay texto, quitar el filtro (mostrar todo)
            sorterOperadores.setRowFilter(null);
        } else {
            // Si hay texto, aplicar filtro con expresión regular
            // (?i) = ignorar mayúsculas/minúsculas
            sorterOperadores.setRowFilter(RowFilter.regexFilter("(?i)" + texto));
        }
    }
    
