    /**
     * MÉTODO: inicializarTablas()
     * -----------------------------------------------------------------------
     * Configura el modelo de la tabla de operadores.
     * 
     * ¿Qué es un DefaultTableModel?
     * Es como una matriz (tabla) que guarda los datos que se muestran en la JTable.
     * Cada fila representa un operador y cada columna un dato (código, nombre, carrera).
     * 
     * ¿Por qué sobrescribir isCellEditable?
     * Para que el usuario NO pueda editar directamente las celdas haciendo doble clic.
     * Solo se pueden modificar usando el botón "Editar".
     */
    private void inicializarTablas() {
        // Paso 1: Definir los nombres de las columnas de la tabla
        String[] columnasOperadores = {"Código", "Nombre", "Carrera"};
        
        // Paso 2: Crear el modelo con las columnas y 0 filas iniciales
        modeloTablaOperadores = new DefaultTableModel(columnasOperadores, 0) {
            
            // Sobrescribir este método para hacer que las celdas NO sean editables
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Siempre retorna false = no editable
            }
        };
        
        // Paso 3: Asignar el modelo a la tabla visual
        TablaOperadores.setModel(modeloTablaOperadores);
    }
    
    /**
     * MÉTODO: cargarOperadoresDesdeArchivo()
     * -----------------------------------------------------------------------
     * Lee el arreglo global de usuarios (Inicio.usuarios) y muestra en la tabla
     * SOLO los usuarios que tienen rol = 2 (Operadores).
     * 
     * Recordatorio de roles:
     * - Rol 1 = Administrador
     * - Rol 2 = Operador
     * - Rol 3 = Usuario normal
     */
    private void cargarOperadoresDesdeArchivo() {
        // Paso 1: Limpiar todas las filas actuales de la tabla
        modeloTablaOperadores.setRowCount(0);
        
        // Paso 2: Recorrer el arreglo global de usuarios
        for (int i = 0; i < Inicio.totalUsuarios; i++) {
            Usuario user = Inicio.usuarios[i];
            
            // Paso 3: Verificar que el usuario existe y es operador (rol = 2)
            if (user != null && user.getRol() == 2) {
                // Paso 4: Crear un arreglo con los datos a mostrar en la fila
                Object[] fila = {
                    user.getCodigo(),    // Columna 0: Código
                    user.getNombre(),    // Columna 1: Nombre
                    user.getCarrera()    // Columna 2: Carrera
                };
                // Paso 5: Agregar la fila al modelo de la tabla
                modeloTablaOperadores.addRow(fila);
            }
        }
        
        // Paso 6: Actualizar la tabla visual con el modelo actualizado
        this.TablaOperadores.setModel(modeloTablaOperadores);
    }
    
    /**
     * MÉTODO: guardarUsuariosEnArchivo()
     * -----------------------------------------------------------------------
     * Guarda todos los usuarios (excepto el admin) en el archivo cuentas.txt.
     * 
     * ¿Por qué no guardamos el admin?
     * Porque el admin está "hardcodeado" en el sistema y no cambia.
     * 
     * Formato de cada línea: rol;codigo;contrasena;nombre;carrera
     * Ejemplo: 2;202012345;pass123;Juan Pérez;Ingeniería
     */
    private void guardarUsuariosEnArchivo() {
        // Paso 1: Crear un arreglo para guardar las líneas del archivo
        String[] lineas = new String[Inicio.totalUsuarios];
        int contador = 0; // Índice para el arreglo de líneas
        
        // Paso 2: Recorrer todos los usuarios del sistema
        for (int i = 0; i < Inicio.totalUsuarios; i++) {
            Usuario user = Inicio.usuarios[i];
            
            // Paso 3: Solo guardar si NO es admin (rol != 1)
            if (user != null && user.getRol() != 1) {
                // Paso 4: Construir la línea con formato: rol;codigo;contrasena;nombre;carrera
                lineas[contador] = user.getRol() + ";" + 
                                  user.getCodigo() + ";" + 
                                  user.getContrasena() + ";" + 
                                  user.getNombre() + ";" + 
                                  user.getCarrera();
                contador++; // Incrementar el contador
            }
        }
        
        // Paso 5: Escribir todas las líneas en el archivo cuentas.txt
        Inicio.archivoCuentas.EscribirArchivo(lineas);
    }

